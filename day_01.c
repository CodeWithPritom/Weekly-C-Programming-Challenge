/*
Problem 1

Write a program to declare and initialize different data types in C (int, float, char, double) and print their values.
*/

#include <stdio.h>

int main()
{

   int i = 10;
   float f = 3.14;
   char c = 'A';
   double d = 2.718281828459;

   printf("Your integer value is: %d\n", i);
   printf("Your float value is: %f\n", f);
   printf("Your character value is: %c\n", c);
   printf("Your double value is: %lf", d);
    


    return 0;
}